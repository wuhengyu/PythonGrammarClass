def bubbleSort(a):
    n = len(a)
    i = 1
    # 11, 23, 14, 56, 27
    while i <= n - 1:
        j = 0
        while j < n - i:
            if a[j] > a[j + 1]:
                a[j], a[j + 1] = a[j + 1], a[j]
            j += 1
        i += 1
    for x in a:
        print(x, end=" ")


