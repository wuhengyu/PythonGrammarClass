# 每比较一轮外循环就要确定一个数，所以内循环n - i，就要少比较一个数
# 第一轮i=1，n-i，5-1=4，内循环4次，确定最大的数56
# 外循环控制轮数，这个数和其他数对比，5个数，要外循环4次，自己不需要和自己比
# 内循环控制所有相邻数进行比较大小的次数，每一轮都减1
