# @Desc    : ord() 函数是 chr() 函数（对于 8 位的 ASCII 字符串）的配对函数，它以一个字符串（Unicode 字符）作为参数，返回对应的 ASCII 数值，或者 Unicode 数值。
'''
语法
ord(c)
参数
c -- 字符。
返回值
返回值是对应的十进制整数。
'''
print(ord('a'))
print(ord('€'))
